    public void GenerarT(){
        try {
            ArrayList<Requerimiento_2> materialesNoPagados = controlador.consultarRequerimiento2();
            for (int i = 0; i < materialesNoPagados.size(); i++) {
                String datos[]= new String[5];
                datos[0]= "" + materialesNoPagados.get(i).getID_proyecto();
                datos[1]= materialesNoPagados.get(i).getProveedor();
                datos[2]= materialesNoPagados.get(i).getPagado();
                datos[3]= "" + materialesNoPagados.get(i).getID_material_construccion();
                datos[4]= materialesNoPagados.get(i).getNombre_material();
                modeloTabla.addRow(datos);
                
            } 
        } catch (SQLException ex) {
            Logger.getLogger(Requerimiento_1tabla.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
